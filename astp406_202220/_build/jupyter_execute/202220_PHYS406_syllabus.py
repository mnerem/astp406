# ## Course Description
# 
# Observational techniques in astronomy with emphasis on constellation identification, celestial movements, and telescopic observation.  Individualized night observations are required.
# 
# This course covers the principles and techniques of modern astronomical data
# acquisition and reduction. The aim is to provide you with a thorough understanding of the concepts
# involved in acquiring and reducing astronomical data, as well as practical experience in image
# processing, photometry, and spectroscopy.

